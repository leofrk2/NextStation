package com.game.nextstation.nextstation.models;

public class Localisation {
    private Integer x;
    private Integer y;

    public Localisation(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }
}
